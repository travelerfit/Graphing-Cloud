<h1 align="center" style="margin-top:120px;"class="page-header">¿Quienes somos?
</h1>
<p style="text-align : justify;">
Graficación en la nube es un proyecto escolar y el primer sitio web que utiliza la computación en la nube (Cloud computing) así como 
la graficación bidimensional y tridimensional, para generar contenido multimedia e interactivo disponible en cualquier lugar dónde exista una conexión a internet, alcanzando así un grado de innovación.
</p>

<h1 class="page-header"><small>Nuestros servicios</small>
</h1>
<p style="text-align : justify;">
Los servicios utilizados en el proyecto son software libre (Processing, HTML5, CSS3, JavaScript, PHP5,  Gungoos Free Cloud Hosting), es por eso, que ofrecemos un espacio disponible para que cada usuario pueda explorar la experiencia de graficar en 2D y 3D de manera gratuita.
</p>

